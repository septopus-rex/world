const Design = {
    block: (x, y, world) => {
        const key = `${x}_${y}`;
        const dt = JSON.parse(JSON.stringify(!blocks[key] ? blocks.empty : blocks[key]));
        return { x: x, y: y, world: world, data: dt, owner: "", };
    },
    show: (x, y, world) => {
        const key = `${x}_${y}`;
        const dt = JSON.parse(JSON.stringify(!septopus[key] ? septopus.empty : septopus[key]));
        return { x: x, y: y, world: world, data: dt, owner: "", };
    },
    texture: (id) => {
        if (!textures[id]) return false;
        const dt = JSON.parse(JSON.stringify(textures[id]));
        dt.index = id;
        return dt;
    },
    model: (id) => {
        if (!models[id]) return false;
        const dt = JSON.parse(JSON.stringify(models[id]));
        dt.index = id;
        return dt;
    },
}

